	<h3>Textbook</h3>
	<p>There is no required textbook for this course. the instructors and most all DJs have utilized the resources around you (internet) in order to learn the technical skills necessary to master the craft.</p>

	<h3>Grading</h3>
	<p>Grading in this course will be completely based on projects and attendance. You must come to class each week and participate. Any more than 2 unexcused absences will result in a failing grade.<p>
	<ul>
		<li>20% Attendance</li>
		<li>20% Weekly in-class assignments</li>
		<li>10% Weekly take-home assignments</li>
		<li>25% Midterm project</li>
		<li>25% Final project</li>
	</ul>
	<p>Weekly assignment grades will be based on effort. If we feel that you tried the assignment and learned something, you will get full credit.  The midterm and final projects will be based on a rubric provided at the time at the assignment. You will have a significant amount of time to complete them.</p>


