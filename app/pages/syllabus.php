    <h3>Course Description</h3>
	<p>This course will be an introduction to DJing, covering the basics of transitions, 
		song selection, beatmixing, and continuity of the overall mix.</p>
	<p>This course should teach you the essentials of DJing and how to be 
		your basic bedroom DJ. From there, we plan to teach you some of the 
		skills that we know can make you a better DJ if you get down into it. 
		We’re going to be doing a lot of practical work in this class 
		(DJing isn’t exactly theoretical) and we’ll try to have distribution 
		instructor attention so that all of you can ask many questions.</p>

	<h3>Typical Course Structure</h3>
	<p> Typical lecture will start with instructor presentations, 
		and then we’ll split into small groups with in class exercises for 
		all of you to do. At these times, you may find the concepts to be 
		hard (especially in the beginning), so feel free to talk to us 
		instructors at any time. </p>
	<p>Please understand that we may modify our course structure in the 
		future. This is the first time this class is being taught, 
		so changes are bound to occur. </p>

	<h3>Course Schedule</h3>
	<p>You will look at the lectures as they appear on a weekly basis. 
		We’re following this schedule:</p>
	<ol>
		<li><strong>Introduction</strong> - Become a basic DJ and learn the 
			absolute essentials (and a little more) to be able 
			to go out there yourself. This would take until spring break, 
			so you can start planning your first gig.</li>
		<li><strong>Teach Me How to DJ.. well</strong> - So you’ve got the skills now, 
			so we’ll look into aspects such as set selection and subtle 
			skills such as mixing into another DJ, working with a Tech Team, 
			that will help you become a better DJ and professional</li>
		<li><strong>Other fun stuff</strong> - We’ll look into other fun things DJs do, 
			like production in Logic, integrating your iPad into DJing, etc. 
			(If you have any suggestions or requests, let us know!)</li>
	</ol>

